export function crayfishTypeSwitch(param) {
    switch(param) {
        case "GALICIAN":
            return "Galicyjski";
        case "SIGNAL":
            return "Sygnałowy";
        case "AMERICAN":
            return "Amerykański";
        case "NOBLE":
            return "Szlachetny";
        default:
            return "WHAT IS THAT MELODY";
    }
}